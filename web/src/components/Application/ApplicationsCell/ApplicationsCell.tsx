import type { FindApplications } from 'types/graphql'

import { Link, routes } from '@redwoodjs/router'
import type { CellSuccessProps, CellFailureProps } from '@redwoodjs/web'

import Applications from 'src/components/Application/Applications'

export const QUERY = gql`
  query FindApplications {
    applications {
      id
      title
      body
      createdAt
    }
  }
`

export const Loading = () => <div>Loading...</div>

export const Empty = () => {
  return (
    <div className="rw-text-center">
      {'No applications yet. '}
      <Link
        to={routes.newApplication()}
        className="rw-link"
      >
        {'Create one?'}
      </Link>
    </div>
  )
}

export const Failure = ({ error }: CellFailureProps) => (
  <div className="rw-cell-error">{error.message}</div>
)

export const Success = ({ applications }: CellSuccessProps<FindApplications>) => {
  return <Applications applications={applications} />
}
